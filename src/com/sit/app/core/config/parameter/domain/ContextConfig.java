package com.sit.app.core.config.parameter.domain;

public class ContextConfig {

	private String contextSOS;
	private String contextSeaport;
	private String contextCAS;
	
	public String getContextSOS() {
		return contextSOS;
	}
	public void setContextSOS(String contextSOS) {
		this.contextSOS = contextSOS;
	}
	public String getContextSeaport() {
		return contextSeaport;
	}
	public void setContextSeaport(String contextSeaport) {
		this.contextSeaport = contextSeaport;
	}
	public String getContextCAS() {
		return contextCAS;
	}
	public void setContextCAS(String contextCAS) {
		this.contextCAS = contextCAS;
	}
	
}
